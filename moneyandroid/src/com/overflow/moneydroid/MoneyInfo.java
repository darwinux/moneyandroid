package com.overflow.moneydroid;

public class MoneyInfo {
	private String _name;
	private String _code;

    // 获取货币名称
    public String getName() 
    {
        return _name;
    }

    // 设置货币名称
    public void setName(String name)
    {
        _name = name;
    }

    


    // 获取货币编码
    public String getCode()
    {
        return _code;
    }

    // 设置货币编码
    public void setCode(String code)
    {
         _code = code;
    }
    
    
}
